#include "main.h"

/**
 * print_times_table - prints n times table
 *
 * @n: n times
 * Return: 0
 */
void print_times_table(int n)
{
	print_times_table(-5);
	_putchar('\n');
	print_times_table(-4);
	_putchar('\n');
	print_times_table(-3);
	_putchar('\n');
	print_times_table(-2);
	_putchar('\n');
	print_times_table(-1);
	_putchar('\n');
	print_times_table(16);
	_putchar('\n');
	print_times_table(17);
	_putchar('\n');
	print_times_table(18);
	_putchar('\n');
	print_times_table(19);
	_putchar('\n');
	print_times_table(n >= 20);
	_putchar('\n');
}
